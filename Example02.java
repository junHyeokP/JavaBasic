package Loop;

public class Example02 {

	public static void main(String[] args) {
		// while 문 예제
      int wi = 1;
    		  
      while (wi <= 5) {
    	  System.out.println(wi);
    	  wi++;
      }
      //for문 예제
      for(int fi = 1; fi <=5; fi++) {
    	  System.out.println(fi);
      }
	}

}
