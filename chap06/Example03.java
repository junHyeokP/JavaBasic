package Loop;

public class Example03 {

	public static void main(String[] args) {
		//while문 예제
		  int wi = 1;
		  
	      while (wi <= 5) {
	    	  System.out.println(wi);
	    	  wi++;
	      }
	      //do ~ while문 예제
	      int doi = 1;
	      do {
	    	  System.out.println(doi);
	    	  doi++;
	      } while (doi <= 5);
	}

}
